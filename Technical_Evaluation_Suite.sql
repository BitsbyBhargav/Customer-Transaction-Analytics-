-- 11. Total Revenue generated by City (Completed Orders Only)
SELECT c.city, SUM(o.amount) AS total_revenue 
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
WHERE o.status = 'Completed'
GROUP BY c.city
ORDER BY total_revenue DESC;

-- 12. Find customers who placed more than 5 orders
SELECT c.name, COUNT(o.order_id) AS order_count 
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
GROUP BY c.name
HAVING COUNT(o.order_id) > 5;

-- 13. Payment method split: Count and Percentage Share
SELECT 
    payment_method, 
    COUNT(payment_method) AS count_per_method, 
    CAST(COUNT(payment_method) * 100.0 / SUM(COUNT(*)) OVER() AS DECIMAL(10,2)) AS percentage_share
FROM payments
GROUP BY payment_method;

-- 14. Find Customers who never placed an order
SELECT c.name, o.order_id 
FROM customers c 
LEFT JOIN orders o ON c.customer_id = o.customer_id
WHERE o.order_id IS NULL;

-- 15. Top 5 best products by quantity sold
SELECT TOP 5 p.product_name, SUM(oi.quantity) AS quantity_sold
FROM products p 
JOIN order_items oi ON p.product_id = oi.product_id
GROUP BY p.product_name
ORDER BY quantity_sold DESC;

-- 16. Average Order Value by membership type comparison
SELECT c.membership AS tier, CAST(AVG(o.amount) AS DECIMAL(10,2)) AS avg_tier_aov
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
GROUP BY c.membership;

-- 17. Monthly revenue for 2024
SELECT DATENAME(MONTH, order_date) AS month_name, SUM(amount) AS monthly_revenue
FROM orders
WHERE YEAR(order_date) = 2024
GROUP BY DATENAME(MONTH, order_date), MONTH(order_date)
ORDER BY MONTH(order_date);

-- 18. Cancellation Rate per city
SELECT 
    c.city, 
    COUNT(CASE WHEN o.status = 'Cancelled' THEN 1 END) AS cancelled_orders, 
    CAST(COUNT(CASE WHEN o.status = 'Cancelled' THEN 1 END) * 100.0 / COUNT(*) AS DECIMAL(10,2)) AS cancellation_rate
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
GROUP BY c.city
ORDER BY cancelled_orders DESC;

-- 19. Second highest order amount (Using DENSE_RANK)
WITH ranked_orders AS (
    SELECT customer_id, order_id, amount, DENSE_RANK() OVER(ORDER BY amount DESC) AS rnk
    FROM orders 
)
SELECT * FROM ranked_orders WHERE rnk = 2;

-- 20. Which product category generates highest revenue per unit sold?
SELECT TOP 1 p.category, SUM(oi.quantity) AS units_sold, SUM(oi.quantity * p.price) AS total_revenue
FROM products p 
JOIN order_items oi ON p.product_id = oi.product_id
GROUP BY p.category
ORDER BY total_revenue DESC;

-- 21. Customers whose total spending exceeds the platform's average AOV
SELECT c.name, SUM(o.amount) AS total_spent 
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
GROUP BY c.name
HAVING SUM(o.amount) > (SELECT AVG(amount) FROM orders);

-- 22. Master Join: Customer, Order, Product, and Payment details
SELECT c.name, o.order_date, p.product_name, oi.quantity, pp.payment_method 
FROM customers c 
JOIN orders o ON c.customer_id = o.customer_id
JOIN order_items oi ON o.order_id = oi.order_id
JOIN products p ON oi.product_id = p.product_id
JOIN payments pp ON o.order_id = pp.order_id;


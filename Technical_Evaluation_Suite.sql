-- 1. List all customers from "Mumbai" with gold membership
SELECT * FROM customers WHERE city='Mumbai' AND membership='Gold';

-- 2. Find the total number of orders placed in 2024
SELECT COUNT(order_id) AS no_of_orders FROM orders WHERE YEAR(order_date)=2024;

-- 3. Show all products priced above 5000 sorted highest to lowest
SELECT product_name, price FROM products WHERE price > 5000 ORDER BY price DESC;

-- 4. Find all orders that are pending or cancelled
SELECT * FROM orders WHERE status IN ('Pending', 'Cancelled');

-- 5. Count how many members belong to each membership type
SELECT membership, COUNT(customer_id) AS mem_cnt FROM customers GROUP BY membership;

-- 6. Find single most expensive order placed
SELECT TOP 1 order_id, amount FROM orders ORDER BY amount DESC;

-- 7. List all the customers who placed orders from Ahmedabad, Surat and Vadodra
SELECT * FROM customers WHERE city IN ('Ahmedabad', 'Surat', 'Vadodra');

-- 8. Find all orders placed in November & December
SELECT * FROM orders WHERE MONTH(order_date) IN (11, 12) ORDER BY order_date;

-- 9. Show the average order value for completed orders
SELECT AVG(amount) AS AOV FROM orders WHERE status='Completed';

-- 10. List products in Electronics or Sports Category, sorted by price
SELECT product_name, price, category FROM products WHERE category IN ('Electronics', 'Sports') ORDER BY price;


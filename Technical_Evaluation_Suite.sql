-- 23. Rank customers by spend with Percentile Logic
WITH spending_listing AS (
    SELECT c.name, SUM(o.amount) AS total_spent, DENSE_RANK() OVER(ORDER BY SUM(o.amount) DESC) AS spent_rnk
    FROM customers c 
    JOIN orders o ON c.customer_id = o.customer_id
    GROUP BY c.customer_id, c.name
),
RankingStats AS (
    SELECT *, MAX(spent_rnk) OVER() AS max_rnk FROM spending_listing 
)
SELECT 
    name, total_spent, spent_rnk,
    CAST((1 - (spent_rnk - 1.0) / max_rnk) * 100 AS DECIMAL(10,2)) AS spend_percentile
FROM RankingStats
ORDER BY spent_rnk;

-- 24. Month over Month (MoM) Revenue Growth Percentage
WITH MonthlyRevenue AS (
    SELECT 
        MONTH(order_date) AS MonthNumber, 
        DATENAME(MONTH, order_date) AS months, 
        SUM(amount) AS monthly_Revenue
    FROM orders 
    GROUP BY MONTH(order_date), DATENAME(MONTH, order_date)
)
SELECT 
    months, monthly_Revenue,
    LAG(monthly_Revenue) OVER (ORDER BY MonthNumber) AS Previous_Month_Revenue,
    CAST((monthly_Revenue - LAG(monthly_Revenue) OVER (ORDER BY MonthNumber)) 
        / NULLIF(LAG(monthly_Revenue) OVER (ORDER BY MonthNumber), 0) * 100 AS DECIMAL(10,2)) AS MoM_Growth_Pct
FROM MonthlyRevenue
ORDER BY MonthNumber;